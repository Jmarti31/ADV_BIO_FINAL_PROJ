(
  Phenotype  VARCHAR(15) NOT NULL,
  taxonomic_rank VARCHAR(10) NOT NULL,
  NCBI_taxon_ID INTEGER NOT NULL,
  Organism_Name VARCHAR(100) NOT NULL,
  USELES VARCHAR(50),
  TRYSS VARCHAR(30),
  BUDBDD VARCHAR(25),
  WOOEN VARCHAR(25),
  WessEN VARCHAR(25),
  phenotype_term VARCHAR(100),
  description TEXT
